//contains data members of each square in the array
public class ArraySquare {
	private double score;
	private int ntrace;
	private int mtrace;
	private int select;

	public ArraySquare() {
		this.score = 0;
	}

	public double getScore() {
		return score;
	}

	public void setScore(double score) {
		this.score = score;
	}

	public int getNtrace() {
		return ntrace;
	}

	public void setNtrace(int ntrace) {
		this.ntrace = ntrace;
	}

	public int getMtrace() {
		return mtrace;
	}

	public void setMtrace(int mtrace) {
		this.mtrace = mtrace;
	}

	public int getSelect() {
		return select;
	}

	public void setSelect(int select) {
		this.select = select;
	}

}